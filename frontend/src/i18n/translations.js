function mergeChatbot(base = {}, enhancement = {}) {
  return {
    ...base,
    ...enhancement,
    quick: { ...(base.quick || {}), ...(enhancement.quick || {}) },
    local: { ...(base.local || {}), ...(enhancement.local || {}) },
    templates: { ...(base.templates || {}), ...(enhancement.templates || {}) },
    actions: { ...(base.actions || {}), ...(enhancement.actions || {}) },
    labels: { ...(base.labels || {}), ...(enhancement.labels || {}) },
  };
}

